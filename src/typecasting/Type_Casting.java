package typecasting;

public class Type_Casting {
public static void main(String[]args)
 {
 int i = 123456;
 byte b = 126;
 float f = 3.14F;
 double d = 314.314;
System.out.println("initial Value of i "+i);
System.out.println("initial Value of b "+b);
b=(byte)i;
System.out.println("Final Value of b "+b);
System.out.println("initial Value of f "+f);
f=(float)d;
System.out.println("Final Value of f "+f);
System.out.println("initial Value of d "+d);
 
 
 } 
}
